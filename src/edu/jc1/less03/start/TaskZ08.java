package edu.jc1.less03.start;

public class TaskZ08 {
    public static void main(String[] args){
        double a = 5;
        double p, P, S;

        p = a * a;
        P = p * 6;
        S = a * a * a;

        System.out.println("Площадь грани = " + p + " см^2");
        System.out.println("Площадь всей поверхности = " + P + " см^2");
        System.out.println("Объём = " + S + " см^3");
    }
}
